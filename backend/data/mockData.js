// Funciones auxiliares - SQLite Style CON TRADUCCIONES
export const getBandById = (id, lang = 'es') => {
  const band = mockData.bands.find(band => band.id === id);
  if (!band) return null;

  return {
    ...band,
    name: band[`name_${lang}`] || band.name,
    schedule: band[`schedule_${lang}`] || band.schedule,
    genre: band[`genre_${lang}`] || band.genre,
    description: band[`description_${lang}`] || band.description,
    country: band[`country_${lang}`] || band.country
  };
};

export const getAllGenres = (lang = 'es') => {
  const genres = [...new Set(mockData.bands.map(band => band[`genre_${lang}`] || band.genre))];
  return genres;
};

export const getNoticiaById = (id, lang = 'es') => {
  const noticia = mockData.noticias.find(noticia => noticia.id === id);
  if (!noticia) return null;

  return {
    ...noticia,
    fecha: noticia[`fecha_${lang}`] || noticia.fecha,
    categoria: noticia[`categoria_${lang}`] || noticia.categoria,
    titulo: noticia[`titulo_${lang}`] || noticia.titulo,
    resumenNoticia: noticia[`resumenNoticia_${lang}`] || noticia.resumenNoticia,
    alt: noticia[`alt_${lang}`] || noticia.alt,
    textoEnlace: noticia[`textoEnlace_${lang}`] || noticia.textoEnlace
  };
};

export const getDetalleNoticiaById = (noticiaId, lang = 'es') => {
  // Buscar por noticia_id en lugar de id directo
  const detalle = mockData.detalleNoticia.find(detalle => detalle.noticia_id === noticiaId);
  if (!detalle) return null;

  return {
    ...detalle,
    fecha: detalle[`fecha_${lang}`] || detalle.fecha,
    categoria: detalle[`categoria_${lang}`] || detalle.categoria,
    titulo: detalle[`titulo_${lang}`] || detalle.titulo,
    descripcionCorta: detalle[`descripcionCorta_${lang}`] || detalle.descripcionCorta,
    contenido: JSON.parse(detalle[`contenido_${lang}`] || detalle.contenido),
    autor: detalle[`autor_${lang}`] || detalle.autor,
    textoBoton: detalle[`textoBoton_${lang}`] || detalle.textoBoton,
    imagenes: JSON.parse(detalle.imagenes),
    videos: JSON.parse(detalle.videos || '[]')
  };
};

